package com.study.ch04;

public class Operation07 {
    public static void main(String[] args) {
        /*
          복합대입연산(산술연산, 대입연산 과 같이 씀)

         */
        int num1 = 10;
        int num2 = 20;
        num1 = num1 + num2; //30
        num1 += num2;//복합대입연산
        num1 *= num2;//자기자신(num1)에서 num2대입해라



    }
}
